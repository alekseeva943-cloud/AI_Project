"""
deduplicator.py

Модуль отвечает за удаление дубликатов текстовых блоков.

Что делает:
1. Создаёт уникальный hash для каждого текста
2. Хранит уже обработанные блоки (локально)
3. Удаляет повторяющиеся элементы

ВАЖНО:
- НЕ использует глобальное состояние
- безопасен для многопользовательской работы
- каждый запуск создаёт новый экземпляр

Это критично для Telegram-бота, где несколько пользователей могут запускать pipeline одновременно.
"""

import hashlib
from app.utils.logger import logger


# =========================
# 🧠 КЛАСС ДЕДУПЛИКАТОРА
# =========================
class Deduplicator:
    """
    Класс для удаления дубликатов.

    Каждый экземпляр хранит свой set,
    поэтому разные пользователи НЕ пересекаются.
    """

    def __init__(self):
        """
        Инициализация.

        Python создаёт:
        self.seen_blocks → пустой set

        Он будет хранить hash уже обработанных текстов.
        """
        self.seen_blocks = set()

        logger.info("🧠 Инициализация дедупликатора (новая сессия)")

    # =========================
    # 🔍 Проверка дубликата
    # =========================

    def is_duplicate(self, text: str) -> bool:
        """
        Проверяет, является ли текст дубликатом.

        Шаги:
        1. приводим к нижнему регистру
        2. кодируем в байты
        3. создаём md5 hash
        4. проверяем в set
        """

        key = hashlib.md5(text.lower().encode()).hexdigest()

        if key in self.seen_blocks:
            return True

        self.seen_blocks.add(key)
        return False

    # =========================
    # 🔄 Обработка списка
    # =========================

    def remove_duplicates(self, content: list) -> list:
        """
        Удаляет дубликаты из списка блоков.

        На вход:
        [
            {"type": "p", "text": "..."}
        ]

        На выход:
        список без повторяющихся элементов
        """

        logger.info("🔁 Удаление дубликатов")

        result = []

        for item in content:
            text = item["text"]

            if self.is_duplicate(text):
                continue

            result.append(item)

        logger.info(f"✅ После дедупликации: {len(result)}")

        return result
