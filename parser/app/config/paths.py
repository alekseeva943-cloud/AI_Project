"""
paths.py

Централизованное хранилище путей parser-подсистемы.

Зачем нужен этот файл:
--------------------------------------------------
Чтобы все пути проекта находились в одном месте.

Если завтра мы захотим:

- перенести output в другую папку;
- изменить расположение cache;
- хранить логи в отдельном месте;
- вынести parser в Docker volume;

нам достаточно будет изменить пути только здесь,
не переписывая десятки файлов по всему проекту.

Архитектурный принцип:
--------------------------------------------------
Bot и Parser являются независимыми подсистемами.

Bot работает только с:

    data/
    backup/

Parser работает только с:

    parser/output/
    parser/cache/
    parser/logs/

Связь между ними происходит только через процесс
активации базы знаний.

Пример:

parser/output/*
        ↓
активация базы
        ↓
data/*
"""

from pathlib import Path


# ==========================================================
# Корень parser-проекта
#
# Получаем абсолютный путь до папки:
#
# parser/
#
# независимо от того, откуда запускается приложение:
#
# uv run main.py
# python main.py
# subprocess из Telegram-бота
# Docker
# ==========================================================
PARSER_ROOT = Path(__file__).resolve().parents[2]


# ==========================================================
# Папка с готовой новой базой знаний.
#
# Именно сюда parser должен складывать:
#
# - metadata.json
# - faiss.index
# - chunks_final.json
# - raw_text.txt
#
# Основной бот никогда сюда не пишет.
# ==========================================================
OUTPUT_DIR = PARSER_ROOT / "output"


# ==========================================================
# Временные данные parser.
#
# Здесь могут храниться:
#
# - html страниц
# - промежуточные json
# - hashes
# - visited urls
#
# Папка может безопасно очищаться.
# ==========================================================
CACHE_DIR = PARSER_ROOT / "cache"


# ==========================================================
# Логи работы parser.
#
# Примеры:
#
# - crawler.log
# - pipeline.log
# - errors.log
#
# Логи не участвуют в работе RAG.
# ==========================================================
LOGS_DIR = PARSER_ROOT / "logs"