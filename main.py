async def post_init(application: Application) -> None:
    """
    Выполняет действия после создания Telegram-приложения.

    Назначение:
    - гарантирует наличие супер-администратора в базе данных;
    - запускает обработку очереди сообщений администратора.

    Args:
        application:
            Экземпляр Telegram-приложения.

    Returns:
        None.
    """

    # Локальный импорт позволяет избежать циклических
    # зависимостей во время запуска проекта.
    from database import (
    DB_PATH,
    add_admin,
)

    logger.info("🚀 Выполняется первоначальная инициализация приложения...")

    # ------------------------------------------------------
    # Гарантируем наличие супер-администратора.
    #
    # Даже если база была удалена или создана заново,
    # главный администратор автоматически будет восстановлен.
    # ------------------------------------------------------

    add_admin(
        db_path=DB_PATH,
        user_id=SUPERADMIN_ID,
        username="Al_leks",
        first_name="Александр",
        last_name=None,
        added_by=SUPERADMIN_ID,
    )

    logger.info(
        "✅ Главный администратор успешно зарегистрирован "
        "(ID: %s).",
        SUPERADMIN_ID,
    )

    # ------------------------------------------------------
    # Запускаем обработку накопившихся сообщений
    # для административной панели.
    # ------------------------------------------------------

    await process_admin_queue(application)

    logger.info("✅ Первоначальная инициализация завершена.")

def setup_handlers(app: Application) -> None:
    """
    Регистрирует все обработчики Telegram-бота.

    Порядок регистрации имеет значение, поскольку Telegram
    последовательно проверяет обработчики до первого совпадения.

    Последовательность регистрации:

    1. Отладочные обработчики.
    2. Базовые команды.
    3. Главное меню.
    4. Раздел услуг.
    5. Автомобильная помощь.
    6. Меню "Нужна помощь".
    7. Административная панель.
    8. Административные обработчики.
    9. GPT-роутер.

    Args:
        app:
            Экземпляр Telegram-приложения.

    Returns:
        None.
    """

    async def debug_all_text(
        update: Update,
        context: ContextTypes.DEFAULT_TYPE,
    ) -> None:
        """
        Логирует все входящие текстовые сообщения.

        Используется исключительно для отладки и помогает
        определить, какие сообщения не были обработаны
        специализированными обработчиками.

        Args:
            update:
                Объект входящего обновления Telegram.

            context:
                Контекст обработки сообщения.

        Returns:
            None.
        """

        if update.message is None:
            return

        logger.info(
            "[FALLBACK] Получено сообщение '%s' от пользователя %s",
            update.message.text,
            update.effective_user.id,
        )

    # ======================================================
    # 1. Отладочный обработчик
    #
    # Регистрируется раньше остальных, чтобы можно было
    # увидеть любое входящее текстовое сообщение.
    # ======================================================

    app.add_handler(
        MessageHandler(
            filters.TEXT,
            debug_all_text,
        ),
        group=-1,
    )

    # ======================================================
    # 2. Базовые команды
    #
    # Обработчики, доступные пользователю сразу после запуска.
    # ======================================================

    # Возврат в предыдущее меню.
    app.add_handler(
        MessageHandler(
            filters.Text(btn.BTN_BACK),
            go_back,
        )
    )

    # Команда /start.
    app.add_handler(
        CommandHandler(
            "start",
            start,
        )
    )

    # Получение контакта пользователя.
    app.add_handler(
        MessageHandler(
            filters.CONTACT,
            handle_contact,
        )
    )

    # ======================================================
    # 3. Главное меню
    #
    # Основные разделы пользовательского интерфейса.
    # ======================================================

    # Раздел помощи.
    app.add_handler(
        MessageHandler(
            filters.Text("🆘 Нужна помощь"),
            show_help_menu,
        )
    )

    # Каталог услуг компании.
    app.add_handler(
        MessageHandler(
            filters.Text("🛠 Наши услуги"),
            handle_services,
        )
    )

    # Контактная информация компании.
    app.add_handler(
        MessageHandler(
            filters.Text("📱 Наши контакты"),
            show_contacts,
        )
    )

    # ======================================================
    # 4. Основные услуги
    #
    # Пользователь выбирает конкретную услугу,
    # после чего отображается её подробное описание.
    # ======================================================

    app.add_handler(
        MessageHandler(
            filters.Text(
                [
                    "🛞 Шиномонтаж",
                    "❄️ Кондиционер",
                    "🚗 Хранение",
                ]
            ),
            show_service_details,
        )
    )

    # ======================================================
    # 5. Автомобильная помощь
    #
    # Сначала открывается подраздел,
    # затем пользователь выбирает необходимую услугу.
    # ======================================================

    # Открытие подменю.
    app.add_handler(
        MessageHandler(
            filters.Text("🔧 Авто-помощь"),
            show_auto_help_submenu,
        )
    )

    # Подробности выбранной услуги.
    app.add_handler(
        MessageHandler(
            filters.Text(
                [
                    "⛽ Подвоз топлива",
                    "💡 Запуск двигателя",
                    "🔒 Отключение сигнализации",
                    "💻 Компьютерная диагностика",
                ]
            ),
            show_auto_help_details,
        )
    )

    # ======================================================
    # 6. Раздел "Нужна помощь"
    #
    # Быстрые сценарии наиболее распространённых
    # проблем пользователей.
    # ======================================================

    app.add_handler(
        MessageHandler(
            filters.Text(
                [
                    "🛞 Спустило колесо",
                    "⛽ Нет топлива",
                    "🚗 Не заводится",
                    "❄️ Отогреть",
                    "🌬 Кондиционер",
                    "⚡ Электрика",
                    "🔓 Вскрыть",
                    "💻 Диагностика",
                    "❓ Прочее",
                    "🔙 Назад",
                ]
            ),
            handle_help_choice,
        )
    )

    # ======================================================
    # 7. Административная панель
    #
    # Точка входа в административное меню.
    # ======================================================

    app.add_handler(
        MessageHandler(
            filters.Text("🛠 Админка"),
            show_admin_panel,
        )
    )

    # ======================================================
    # 8. Административные обработчики
    #
    # Все обработчики регистрируются централизованно,
    # чтобы main.py не зависел от их внутренней реализации.
    # ======================================================

    for handler in get_admin_handlers():
        app.add_handler(handler)

    # ======================================================
    # 9. GPT-роутер
    #
    # Этот обработчик всегда регистрируется последним.
    #
    # До него доходят только те сообщения,
    # которые не были обработаны предыдущими
    # специализированными обработчиками.
    # ======================================================

    app.add_handler(
        MessageHandler(
            (
                filters.TEXT
                | filters.CONTACT
                | filters.LOCATION
            )
            & ~filters.COMMAND,
            handle_gpt_query,
        )
    )

def main() -> None:
    """
    Создаёт и запускает Telegram-бота.

    Последовательность работы:

    1. Создаётся экземпляр Telegram-приложения.
    2. Выполняется первоначальная инициализация.
    3. Регистрируются все обработчики.
    4. Запускается получение обновлений через polling.

    Returns:
        None.
    """

    logger.info("🚀 Запуск Telegram-бота...")

    try:
        # --------------------------------------------------
        # Создаём экземпляр Telegram-приложения.
        #
        # post_init будет автоматически вызван после
        # успешного создания Application.
        # --------------------------------------------------

        application = (
            Application.builder()
            .token(TELEGRAM_TOKEN)
            .post_init(post_init)
            .build()
        )

        logger.info("✅ Telegram-приложение успешно создано.")

        # --------------------------------------------------
        # Регистрируем все обработчики сообщений.
        # --------------------------------------------------

        logger.info("📦 Регистрация обработчиков...")

        setup_handlers(application)

        logger.info("✅ Обработчики успешно зарегистрированы.")

        # --------------------------------------------------
        # Запускаем получение обновлений Telegram.
        #
        # Метод run_polling() блокирует поток до момента
        # завершения работы приложения.
        # --------------------------------------------------

        logger.info("🤖 Бот готов к работе.")

        application.run_polling()

    except KeyboardInterrupt:
        # Пользователь остановил приложение вручную.

        logger.info("🛑 Работа бота остановлена пользователем.")

    except Exception:
        # logger.exception автоматически выводит полный
        # стек вызовов, что значительно упрощает поиск
        # причины критических ошибок.

        logger.exception(
            "❌ Во время запуска Telegram-бота "
            "произошла критическая ошибка."
        )

        raise


# ==========================================================
# Точка входа программы.
#
# При запуске файла напрямую создаётся экземпляр приложения
# и начинается работа Telegram-бота.
# ==========================================================

if __name__ == "__main__":
    main()