# Назначение:
# Централизованное хранение текстов всех кнопок проекта.
#
# Зачем нужен:
#
# - текст каждой кнопки существует только в одном месте;
# - исчезают ошибки вида:
#       "❌ Отмена" != "⬅️ Отмена";
# - изменение текста производится один раз;
# - удобно использовать в KeyboardButton,
#   MessageHandler(filters.Text()),
#   проверках if text == ...
# - упрощается поддержка больших проектов.
#
# Пример использования:
#
# from config.buttons import BTN_CANCEL
#
# if text == BTN_CANCEL:
#     ...
#
# KeyboardButton(BTN_CANCEL)
#
# MessageHandler(filters.Text(BTN_CANCEL), handler)
#
# ==========================================================


